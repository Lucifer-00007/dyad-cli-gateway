const express = require('express');
const authRoute = require('./auth.route');
const userRoute = require('./user.route');
const docsRoute = require('./docs.route');
const config = require('../../config/config');

// Import gateway routes
const { v1Routes: gatewayRoutes, adminRoutes: gatewayAdminRoutes } = require('../../gateway/routes');

const router = express.Router();

const defaultRoutes = [
  {
    path: '/auth',
    route: authRoute,
  },
  {
    path: '/users',
    route: userRoute,
  },
];

// Gateway routes (OpenAI-compatible endpoints)
const gatewayRoutesConfig = [
  {
    path: '/',
    route: gatewayRoutes,
  },
  {
    path: '/admin',
    route: gatewayAdminRoutes,
  },
];

const devRoutes = [
  // routes available only in development mode
  {
    path: '/docs',
    route: docsRoute,
  },
];

defaultRoutes.forEach((route) => {
  router.use(route.path, route.route);
});

// Mount gateway routes
gatewayRoutesConfig.forEach((route) => {
  router.use(route.path, route.route);
});

/* istanbul ignore next */
if (config.env === 'development') {
  devRoutes.forEach((route) => {
    router.use(route.path, route.route);
  });
}

module.exports = router;
